"""## **Create Grade Labels from G3**"""

def grade_label(g3):
    if g3 >= 16:   return 'A (Excellent)'
    elif g3 >= 14: return 'B (Good)'
    elif g3 >= 12: return 'C (Average)'
    elif g3 >= 10: return 'D (Satisfactory)'
    else:          return 'F (Fail)'

df['Grade'] = df['G3'].apply(grade_label)
df['Pass']  = (df['G3'] >= 10).astype(int)

